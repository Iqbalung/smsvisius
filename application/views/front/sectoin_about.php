              <p class="sep-top-sm sep-bottom-sm">
               Tidak ada anak yang bodoh. Semua anak itu cerdas. Cerdas  sesuai
dengan bakat dan minatnya. Ada yang cerdas matematika. Cerdas 
bahasa. Cerdas bergerak. Cerdas musik. Cerdas Warna.  Untuk itu, 
sudah menjadi tanggung jawab kita untuk terus meningkatkan dan 
mengembangkan kecerdasan anak. Salah satunya melalui pendidikan 
kreatif.  Sebabnya, saat berkreasi ini,  sesungguhnya anak sedang 
menggunakan kecerdasannya untuk menghasilkan karya. Maka, kreatif 
berarti kecerdasan yang selalu digunakan. Anak yang kreatif pastilah 
anak yang cerdas.  Dalam berkreativitas inilah,  karakter anak-anak 
yang baik akan dibentuk. Jadi, cerdas, kreatif, dan karakter tidak 
dipisahkan.


Dengan demikian, menciptakan pendidikan  melalui kegiatan-kegiatan 
kreatif  bagi anak harus selalu kita lakukan [sebagai guru, orangtua, 
dan masyarakat].  Sebab melalui kegiatan kreatif  ini anak-anak 
mampu menuangkan ide gagasan dalam bentuk aktivitas kegiatan 
yang berulang dan berpola sehingga akan menciptakan anak-anak 
yang berkarakter. Di sinilah, pendidikan yang kreatif secara intensif 
akan melahirkan generasi anak-anak masa depan yang cerdas, kreatif, 
dan berkarakter. 


Di sisi lain, pendidikan kreatif yang menyenangkan, yang selalu 
diselenggarakan setiap hari akan membuat anak-anak kita tidak 
terimbas oleh dampak negatif teknologi, televisi, dunia gadget, game, 
dan pergaulan bebas karena melalui pendidikan kreatif anak-anak 
diorganisasi untuk bermain secara menyenangkan dan mendidik. Di 
sinilah, di masa sekarang pendidikan kreativitas yang berbasis soal 
masyarakat sudah urgen untuk diselenggarakan. Hal ini senada dengan 
pinsip pendidikan yang Anies Baswedan [Kemendikbud] anjurkan 
bahwa penyelenggaraan pendidikan substansinya ada di masyarakat. 
Arti penting ini yang menjadi semangat kami untuk terus mendidik 
anak-anak melalui lembaga sosial masyarakat, yaitu RUMAH KREATIF 
WADAS KELIR.


Pada 23 Juli  2011 [Hari Anak Nasional], Saya mengajak anak-anak tetangga untuk 
bermain di rumah saya [di Perumahan Griya Mulawarman Indah, Karangklesem]. 
Saya mengisi dengan kegiatan Mendongeng dan Membaca. Kegiatan ini yang 
awalnya diikuti lima anak berkembang menjadi lima belas anak. Setiap Senin – 
Kamis, mulai pukul 19.00 – 21.00 WIB saya mengajar kreativitas anak-anak. Mulai 
dari Menulis, Membaca, Bermain Drama, Menggambar, dan sebagainya. Pada 1 Juli 
2013  saya dan keluarga pindah rumah di  desa [Jln,. Wadas Kelir 07/05 
Karangklesem]. Saya melanjutkan kegiatan pendidikan kreativitas untuk anak-
anak desa dengan nama RUMAH KREATIF WADAS KELIR [selanjutnya  RKWK].  
Saya kemudian  berhasil mengajak dua puluh anak bergabung, yang sekarang 
berkembang menjadi lima puluh anak dari lingkungan desa sendiri dan dua puluh  
anak dari luar.  Dengan banyaknya jumlah anak-anak yang belajar, saya membuka 
kesempatan siapa saja untuk menjadi relawan, dan lima belas mahasiswa  menjadi 
relawan  yang membimbing dan mengajar di RKWK. 

Dari sinilah, saya bersama tim kreatif membuat dan merumuskan visi dan misi, 
tujuan, kurikulum, kegiatan-kegiatan, model dan pendekatan pembelajaran, dan 
sebagainya. Hasilnya kegiatan pendidikan kreatif  RKWK pun tersistematisasi 
dengan baik. Dan anak-anak terus  tumbuh-kembang dalam kreativitas yang tanpa 
batas dalam bimbingan dan pendampingan kami.  Kini RKWK , atas bantuan  
PRINGSEWU GROUP , pada 18 September 2014 dilegalkan menjadi  YAYASAN 
WADAS KELIR [Yayasan Sosial Kemanusiaan Nonprofit] dengan Nomor AHU-
0006240.50.80.2014.

RKWK kini telah mendampingi anak-anak belajar kreativitas selama tiga tahun 
lebih, dan waktu yang masih singkat itu telah tumbuh menjadi anak-anak yang 
kreatif, cerdas, dan berkarakter. Anak-anak sudah  tahu akan potensi kreativitas 
dan kecerdasannya masing-masing, anak-anak berprestasi di sekolahnya, bisa 
masuk sekolah-sekolah favorit karena  kreativitasnya,  memiliki prestasi 
kreativitas sampai tingkat nasional, dan anak-anak telah tumbuh menjadi anak-
anak yang pemberani, bersahabat, baik, memiliki jiwa kepemimpinan yang tinggi, 
cerdas , dan kreatif. Padahal dulunya mereka adalah anak-anak pedesaan yang 
minder, tidak saling kenal, beberapa anak hampir putus sekolah, tak punya cita-
